const int INF = 1 << 30;
const ll INFL = 1LL << 60;


class Solver {
  public:
    bool solve() {
        string S; cin >> S;
        int N; cin >> N;
        reverse(all(S));
        int ans = 0;
        int non_zero = 0;
        for(int i = 0; i < S.size() and N > 0; i++) {
            if(S[i] == '0') N--;
            else ans++;
        }
        for(char c : S) non_zero += c != '0';
        int a = non_zero < S.size() ? S.size() - 1 : S.size();
        cout << min<int>(a, (N == 0 ? ans : INF)) << endl;
        return 0;
    }
};

int main() {
    cin.tie(0);
    ios::sync_with_stdio(false);
    Solver s;
    s.solve();
    return 0;
}
